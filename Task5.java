package Homework1;

public class Task5 {
    public static void main(String[] args) {
        System.out.println("From 1 to 100:");
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        System.out.println("Start over");

        System.out.println("From 100 to 1:");
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
    }
}